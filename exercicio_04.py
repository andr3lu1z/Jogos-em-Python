#A imobiliária Imóbilis vende apenas terrenos retangulares. Faça um algoritmo para ler as 
# dimensões de um terreno e depois exibir a área do terreno. 


lado_1 = float(input("Digite a dimensão do lado 1 do terreno:"))

lado_2 = float(input("Digite a dimensão do lado 2 do terreno:"))


area = lado_1 * lado_2

print("A área do terreno é: ", area)
